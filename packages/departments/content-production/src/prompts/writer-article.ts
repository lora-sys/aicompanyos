/**
 * 辅助函数：基于任务上下文构建完整的 User Prompt
 * 将 ARTICLE_WRITER_PROMPT 作为 system prompt，此函数返回 user prompt 部分
 */
export function buildArticleWriterUserPrompt(params: {
  topic: string;
  targetPlatform: "wechat" | "zhihu";
  keywords: string[];
  internalLinks?: Array<{ text: string; url: string }>;
  researchContext?: string;
  tone?: "professional" | "insightful" | "tutorial" | "opinion";
  previousDraft?: string;
  criticFeedback?: string;
  rewriteRound?: number;
}): string {
  let prompt = "";

  // 任务基本信息
  prompt += `## 写作任务\n`;
  prompt += `- **主题**：${params.topic}\n`;
  prompt += `- **目标平台**：${params.targetPlatform === "wechat" ? "微信公众号" : "知乎专栏"}\n`;
  prompt += `- **主关键词**：${params.keywords.join("、")}\n\n`;

  // 语调偏好
  if (params.tone) {
    const toneMap: Record<string, string> = {
      professional: "专业严谨型（偏学术/白皮书风格）",
      insightful: "洞察分析型（偏评论/深度解读风格）",
      tutorial: "教程实操型（偏指南/手把手教学风格）",
      opinion: "观点表达型（偏社论/个人立场风格）",
    };
    prompt += `## 语调偏好\n${toneMap[params.tone] ?? params.tone}\n\n`;
  }

  // 内链资源
  if (params.internalLinks && params.internalLinks.length > 0) {
    prompt += `## 可用的内链资源\n`;
    params.internalLinks.forEach((link, i) => {
      prompt += `${i + 1}. [${link.text}](${link.url})\n`;
    });
    prompt += "\n";
  }

  // 研究资料
  if (params.researchContext) {
    prompt += `## 研究参考资料\n${params.researchContext}\n\n`;
  }

  // 重写模式：注入上一版草稿和 Critic 反馈
  if (params.previousDraft || params.criticFeedback) {
    if (params.rewriteRound && params.rewriteRound > 1) {
      prompt += `> ⚠️ 这是第 ${params.rewriteRound} 轮重写，你必须根据 Critic 反馈逐条修改。\n\n`;
    }
    if (params.previousDraft) {
      prompt += `## 上一版草稿\n${params.previousDraft}\n\n`;
    }
    if (params.criticFeedback) {
      prompt += `${"═".repeat(60)}\n`;
      prompt += `## 🔴 CRITIC 审核反馈（必须逐条回应）\n`;
      prompt += `${"═".repeat(60)}\n\n`;
      prompt += params.criticFeedback;
      prompt += `\n\n**【重写要求】**\n`;
      prompt += `- 针对每一条 Critic 建议进行修改\n`;
      prompt += `- 保留评分高的部分，只改进被指出的问题\n`;
      prompt += `- 确保修改后仍满足所有篇幅和主题约束\n\n`;
    }
  }

  prompt +=
    "\n请严格遵循系统提示中的全部规范，输出完整的高质量 Markdown 长文。";

  return prompt;
}
